//유자 클래스 : 속성  has a관계
public class Yuja {
   
   private int yuja;
   private int sugar;
   
   //setter, getter
   public void setYuja(int y) {
      yuja=y;
   }
   public void setSugar(int s) {
      sugar=s;
   }
   public int getYuja() {
      return yuja;
   }
   public int getSugar() {
      return sugar;
   }
}//////////////////////////////////
package io.day3;
import java.io.*;
/*키보드 입력==> 콘솔에 출력
 * BufferedReader/PrintWriter
 * - 2byte기반
 * - 필터 스트림
 * - 버퍼에 모아서 읽고/쓴다
 * - int read()
 * - String readLine()
 * ------------------------------
 * 데이터 소스	 : 키보드==>노드스트림(System.in)
 * 데이터 목적지: 콘솔 	==>노드스트림(System.out)
 * */
public class StandardinOut2 {

	public static void main(String[] args)
	throws Exception{
		BufferedReader br
		=new BufferedReader(new InputStreamReader(System.in));
		//BufferedReader는 생성자에 Reader계열의 객체를 받아들인다.
	    //따라서 1바이트기반 스트림과 연결해야 할 때는 브릿지 스트림이 필요하다.
		
		PrintWriter pw
		=new PrintWriter(System.out, true);//true값을 주면 auto flush를 지원함
		//단, 줄바꿈 문자(\n)를 만났을 때 자동으로 flush한다.
		
		String line="";
		
		while((line=br.readLine())!=null) {
			/*pw.write(line);
			pw.flush();*/
			pw.println(line);//=>위에 처럼 할 필요 없이 자동으로 flush됨
		}
		br.close();
		pw.close();
		
	}

}
